"<b>Химическое равновесие</b> — состояние обратимой реакции, при котором скорость прямой реакции равна скорости обратной, а концентрации всех веществ реакционной смеси перестают меняться со временем. Реакционная смесь в этом состоянии называется <b>равновесной</b>.\n\n"
