"• K ≫ 1 — равновесие смещено в сторону продуктов (реакция идёт «слева направо»);\n"
"• K ≪ 1 — равновесие смещено в сторону исходных веществ;\n"
"• K &gt; 10⁶ — реакцию можно считать практически необратимой (идёт до конца);\n"
"• K ≈ 1 — в равновесной смеси значимые количества и реагентов, и продуктов.\n\n"
