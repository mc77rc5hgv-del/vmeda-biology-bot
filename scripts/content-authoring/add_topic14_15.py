"<b>Обратимые и необратимые реакции:</b>\n\n"
"<b>Необратимые реакции</b> протекают до полного израсходования одного из реагентов (например, Zn + 2HCl → ZnCl₂ + H₂↑ — цинк или кислота расходуются полностью).\n\n"
